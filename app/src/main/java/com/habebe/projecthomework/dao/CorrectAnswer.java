package com.habebe.projecthomework.dao;

import com.habebe.projecthomework.dao.homeworkset.Answer;
import com.habebe.projecthomework.dao.homeworkset.Question;

public class CorrectAnswer {
    public String CORRECTID;
    public String ANSWERID;
    public String ANSDESC;
    public Question question;
    private Answer answer;

    public String getCORRECTID() {
        return CORRECTID;
    }

    public void setCORRECTID(String CORRECTID) {
        this.CORRECTID = CORRECTID;
    }

    public String getANSWERID() {
        return ANSWERID;
    }

    public void setANSWERID(String ANSWERID) {
        this.ANSWERID = ANSWERID;
    }

    public String getANSDESC() {
        return ANSDESC;
    }

    public void setANSDESC(String ANSDESC) {
        this.ANSDESC = ANSDESC;
    }

    public Question getQuestion() {
        return question;
    }

    public void setQuestion(Question question) {
        this.question = question;
    }

    public Answer getAnswer() {
        return answer;
    }

    public void setAnswer(Answer answer) {
        this.answer = answer;
    }
}
