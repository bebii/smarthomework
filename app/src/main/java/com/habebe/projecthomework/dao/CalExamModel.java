public class CalExamModel {
    private boolean isMHeader;
    private String MontYear;
    private String NumDate;
    private String NameDate;
    private String Subject;
    private String Chapter;
    private String Exercise;

    public boolean isMHeader() {
        return isMHeader;
    }

    public void setMHeader(boolean MHeader) {
        isMHeader = MHeader;
    }

    public String getMontYear() {
        return MontYear;
    }

    public void setMontYear(String montYear) {
        MontYear = montYear;
    }

    public String getNumDate() {
        return NumDate;
    }

    public void setNumDate(String numDate) {
        NumDate = numDate;
    }

    public String getNameDate() {
        return NameDate;
    }

    public void setNameDate(String nameDate) {
        NameDate = nameDate;
    }

    public String getSubject() {
        return Subject;
    }

    public void setSubject(String subject) {
        Subject = subject;
    }

    public String getChapter() {
        return Chapter;
    }

    public void setChapter(String chapter) {
        Chapter = chapter;
    }

    public String getExercise() {
        return Exercise;
    }

    public void setExercise(String exercise) {
        Exercise = exercise;
    }
}
