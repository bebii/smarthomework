public class StatModel {
    private String Subject;
    private String Exercise_Total;
    private String Exercise_Correct;
    private String Exercise_Wrong;
    private String Exercise_Donot;

    public String getSubject() {
        return Subject;
    }

    public void setSubject(String subject) {
        Subject = subject;
    }

    public String getExercise_Total() {
        return Exercise_Total;
    }

    public void setExercise_Total(String exercise_Total) {
        Exercise_Total = exercise_Total;
    }

    public String getExercise_Correct() {
        return Exercise_Correct;
    }

    public void setExercise_Correct(String exercise_Correct) {
        Exercise_Correct = exercise_Correct;
    }

    public String getExercise_Wrong() {
        return Exercise_Wrong;
    }

    public void setExercise_Wrong(String exercise_Wrong) {
        Exercise_Wrong = exercise_Wrong;
    }

    public String getExercise_Donot() {
        return Exercise_Donot;
    }

    public void setExercise_Donot(String exercise_Donot) {
        Exercise_Donot = exercise_Donot;
    }
}
